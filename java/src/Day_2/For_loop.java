package Day_2;

public class For_loop {
    public static void main(String[] args){
        // for ( start ; condition ; step ) { code }
        for(int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // reverse loop
        for(int i = 10 ; i >=1 ; i--){
            System.out.println(i);
        }

    }
}
